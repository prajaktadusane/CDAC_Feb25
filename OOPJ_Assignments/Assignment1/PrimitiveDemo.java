/* Create a program that declares and initializes all primitive data types in Java and prints their 
default and assigned values.*/


public class PrimitiveDemo{
	public static void main(String[] args){
		
		byte b1 = 50;
		short s1 = 100;
		int i1 = 2000;
		long l1 = 20000L;
		float f1 = 20.4f;
		double d1 = 40.5;
		char c1 = 'A';
		boolean bn1 = true;
		
		System.out.println("Values of Primitive Data types:");
		System.out.println("byte value" +b1);
		System.out.println("short value " +s1);
		System.out.println("int value " +i1);
		System.out.println("long value " +l1);
		System.out.println("float value" +f1);
		System.out.println("double value " +d1);
		System.out.println("char value" +c1);
		System.out.println("boolean value " +bn1);
		
	}
}